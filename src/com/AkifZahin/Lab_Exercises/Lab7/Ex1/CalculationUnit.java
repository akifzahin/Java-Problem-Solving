public class CalculationUnit
{
	public int addition(int a, int b)
	{
		int add = a+b;
		return add;
	}
	public int subtraction(int a, int b)
	{
		int sub = a-b;
		return sub;
	}
	public int multiply(int a, int b)
	{
		int multiply = a*b;
		return multiply;
	}
}
